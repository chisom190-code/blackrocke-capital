type LangTranslations = typeof translations['en'];
type AllTranslations = { [key: string]: Partial<LangTranslations> };
const allTranslations: AllTranslations = translations;

type LanguageContextType = {
  lang: string;
  setLang: (code: string) => void;
  t: (key: TranslationKey) => string;
  isRTL: boolean;
  currentLanguage: Language;
};

const LanguageContext = createContext<LanguageContextType>({
  lang: 'en',
  setLang: () => {},
  t: (key) => key,
  isRTL: false,
  currentLanguage: LANGUAGES[0],
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState('en');

  useEffect(() => {
    const saved = localStorage.getItem('brc_lang') || 'en';
    setLangState(saved);
  }, []);

  const setLang = (code: string) => {
    setLangState(code);
    localStorage.setItem('brc_lang', code);
  };

  const t = (key: TranslationKey): string => {
    const langData = allTranslations[lang] || allTranslations['en'];
    return (langData[key] as string) || (allTranslations['en'][key] as string) || key;
  };

  const currentLanguage = LANGUAGES.find(l => l.code === lang) || LANGUAGES[0];
  const isRTL = currentLanguage.rtl;

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, isRTL, currentLanguage }}>
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => useContext(LanguageContext);
